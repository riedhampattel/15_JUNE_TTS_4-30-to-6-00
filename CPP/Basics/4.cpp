#include<iostream>
using namespace std;
int main()
{
	int x;
	double p;
	char ch;
	string msg;
	
	//input in cpp
	cout<<"\nEnter the value of integer x = ";
	cin>>x;
	cout<<"\nEnter the value of double p = ";
	cin>>p;
	cout<<"\nEnter the value of char ch = ";
	cin>>ch;
	cout<<"\nEnter the value of string msg = ";
	cin>>msg;
	
	//output in cpp
	cout<<"\nValue of integer = "<<x;
	cout<<"\nValue of double = "<<p;
	cout<<"\nValue of character = "<<ch;
	cout<<"\nValue of string = "<<msg;
	
	return 0;
}
